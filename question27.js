console.log("When the alien is Green, print a message that ");
let alien_colors = "green";
if (alien_colors === "green") {
    console.log("Congratulations, You have earned 5 points");
}
else if (alien_colors === "yellow") {
    console.log("Congratulations, You have earned 10 points");
}
else if (alien_colors === "red") {
    console.log("Congratulations, You have earned 15 points");
}
else {
    console.log("Unknown Alien Color");
}
